
public class CharTest {

	public static void main(String[] args) {
		char c = 'A';
		System.out.println(c);
		
		c = 65;
		System.out.println(c);
		
		c = '\u0041';
		System.out.println(c);
		
		c = '\u092A'; //Hindi
		System.out.println(c);
		
		c = '\u4eca'; //Japanese
		System.out.println(c);
	}
}
